int main(void)
{

  /* USER CODE BEGIN 1 */
	char str[16] = {0};
  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_USART1_UART_Init();
  MX_RTC_Init();
  MX_I2C1_Init();

  /* USER CODE BEGIN 2 */
	HAL_RTC_Init(&hrtc);
	HAL_RTCEx_SetSecond_IT(&hrtc);
	
	uint8_t uartTransmit[] = "USART OK\r\n";
	HAL_UART_Transmit_DMA(&huart1, uartTransmit, sizeof(str));
	
	HD44780_Init();
	HD44780_Clear();
	HD44780_SetPos(0, 0);
	HD44780_String("*** STRING 0 ***");
	HD44780_SetPos(1, 0);
	HD44780_String("*** STRING 1 ***");
	HAL_Delay(1000);
	HD44780_Clear();
	
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
		if(hdma_usart1_rx.State == HAL_DMA_STATE_READY)
		{
		HAL_UART_Receive_DMA(&huart1, (uint8_t*) str, sizeof(str));
		hdma_usart1_rx.State = HAL_DMA_STATE_BUSY;
		HD44780_SetPos(0, 0);
		HD44780_String(str);
		HAL_Delay(2000);
		HD44780_Clear();
		HD44780_SetPos(0, 0);
		}
		
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
		if (lcd_out)
		{
			lcd_out = false;
			
			HAL_RTC_GetTime(&hrtc, &clkTime, RTC_FORMAT_BCD);
			rtcSec = bcdToDec(clkTime.Seconds);
			rtcMin = bcdToDec(clkTime.Minutes);
			rtcHrs = bcdToDec(clkTime.Hours);

			sprintf(str, "%.2x:", clkTime.Hours);
			sprintf(bufstr, "%.2x:", clkTime.Minutes);
			strcat(str, bufstr);
			sprintf(bufstr, "%.2x", clkTime.Seconds);
			strcat(str, bufstr);
			HD44780_SetPos(0, 0);
			HD44780_String(str);
			str[0] = 0;
		}
		
		if(HAL_GPIO_ReadPin(GPIOB, GPIO_PIN_3) == GPIO_PIN_SET)
		{

			HD44780_Clear();
			HAL_Delay(500);
		}
		
		if(HAL_GPIO_ReadPin(GPIOB, GPIO_PIN_4) == GPIO_PIN_SET)
		{
			clkTime.Hours = clkTime.Hours++;
			HAL_RTC_SetTime(&hrtc, &clkTime, RTC_FORMAT_BCD);
			HAL_Delay(500);
		}
		
		if(HAL_GPIO_ReadPin(GPIOB, GPIO_PIN_5) == GPIO_PIN_SET)
		{
		clkTime.Minutes = clkTime.Minutes++;
		clkTime.Seconds = 0x00;
		HAL_RTC_SetTime(&hrtc, &clkTime, RTC_FORMAT_BCD);
		HAL_Delay(500);
		}
		
		if(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_15) == GPIO_PIN_SET)
		{
		clkTime.Hours = decToBcd(rtcSetHrs);
		clkTime.Minutes = decToBcd(rtcSetMin);
		clkTime.Seconds = decToBcd(rtcSetSec);

		HAL_RTC_SetTime(&hrtc, &clkTime, RTC_FORMAT_BCD);
		HAL_Delay(300);
		}
		
  }
  /* USER CODE END 3 */

}

